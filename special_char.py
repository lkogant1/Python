#special characters
print("welcome to xyz")

#backslash \
print("welcome \\ to \\ xyz")
#single quote \'
print("welcome \' to \' xyz")
#double quote \"
print("welcome \" to \" xyz")
#bell \a
print("welcome \a to \a xyz")
#backspace \b
print("wel\bcome to x\byz")
#newline \n
print("welcome \n to \n XYZ")
#carriage return \r
print("welcome to \r to \r xyz")
#tab space character \t
print("welcome \t to \t xyz")
#vertical tab
print("welcome \v to \v xyz")




